package Operator;
//Five operator:
/* 1. Addition +
   2. Subraction -
   3. Multiplication *
   4. Division /
   5. Modules %  */ 
public class ArithmeticOperator {
    public static void main(String[] args) {
        int a=20;
        int b=11;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
