package Operator;
//it returns boolean value
//Six Operator:
/* 1.==
   2.!=
   3.>
   4.>=
   5.<
   6.<= */
public class ComparisonOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
