package Classandobject;

public class SampleExplain { //SampleExplain - Userdefined class
    public static void main(String[] args) {  //public - Predefined class , String[]args - Parameter
      System.out.println("Java");  //System - class , out - reference , println - method
    }
}

/* public - Access modifier
   static - keyword(memory allocation while class loading)
   void -  non return type
   main - method name
   string[] - can give multiple string inputs */
